package Java_practices;

public class EscapeCharacters {
	public static void main(String[] args) {
		System.out.println("AB\tCD");
		//\t =tab
		System.out.println("AB\bCD");
		//\b = backSpace
		System.out.println("AB\nCD");
		//\ n = New.line
		System.out.println("AB\\CD");
		//\\ = backslash
		System.out.println("\"");
		//\" = Double quote	
	}
}

