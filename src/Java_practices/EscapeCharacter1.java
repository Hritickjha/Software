package Java_practices;

public class EscapeCharacter1 {
	public static void main(String[] args) {
		System.out.println("ABC\rDE"); //DEC
		//carriage return-\r
		/*Brings cursor to the beginning of the line and prints the next part over the initial print*/
	System.out.println("AB/fcD");
	// for feed-\f
	/* used for page breakinf in printers*/
	}

}

